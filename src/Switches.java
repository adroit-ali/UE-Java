public class Switches {
    public static void main(String[] args){
        int x = 2;
        switch (x){
            case 1:
                System.out.println("A");
                break;
            case 2:
                System.out.println("B");
                break;
            case 3:
                System.out.println("C");
                break;
            default:
                System.out.println("no option");
        }
        System.out.println("after switch");
    }
//    after ...
}
