public class Variables {
    public static void main(String[] args){
        int x = 45; // integer
        char a = 'A'; // character
        double b = 5.7; // float,double 1.43587436
        String e = "dsadlaskl"; // Set of Character
        boolean n = false;  // true or false
        System.out.println("This is integer output: "+x);
        System.out.println("This is Character output: "+a);
        System.out.println("This is double output: "+b);
        System.out.println("This is String output: "+e);
        System.out.println("This is Boolean output: "+n);
        System.out.print("1");
        System.out.print("new hai");
    }
}
